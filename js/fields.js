export class Field {

  constructor () {
    this._image = new Image();
    this._image.src = "mission.png";

    // scale: 2mm per px
    this._width = 3657/2;
    this._height = 3657/2;
  }

  //----------------------------------------------------------

  get image () {
    return this._image;
  }

  get width () {
    return this._width;
  }

  get height () {
    return this._height;
  }
  
}

